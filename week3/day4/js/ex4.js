// Упражнениe 1 Создайте объект вида { a: 1, b: 2, c: 3 }. Напишите функцию, которая выводит в консоль ключи и значения объекта в формате “${key - value}”
let exOneObj = {
    a: 1,
    b: 2,
    c: 3,
}

function show(obj) {
    for (const [key, value] of Object.entries(obj)) {
        console.log("${" + key + " - " + value + "}");
    }
}

show(exOneObj);

